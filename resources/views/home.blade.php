                $.ajax({
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    type: 'POST',
                    url: "{{route('approveUser')}}",
                    dataType: 'json',
                    data : {userID :userID,response:response},
                    success: function(data) {
                        location.reload();
                    }
                });
